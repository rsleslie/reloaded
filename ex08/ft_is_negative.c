#include <unistd.h>

void	ft_putchar(char c);

void	ft_is_negative(int n)
{
	char	negative;
	char	positive;

	negative = 'N';
	positive = 'P';
	if (n < 0)
		ft_putchar(negative);
	if (n >= 0)
		ft_putchar(positive);
}
